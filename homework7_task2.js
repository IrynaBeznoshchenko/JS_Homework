/*Задача 2. Сума вартості послуг

 Припустимо, у нас є об'єкт. Властивості об'єкта - назва послуги та її вартість, вигляду:

var services = {

"стрижка": "60 грн",

"гоління": "80 грн",

"Миття голови": "100 грн"

};

"Послуги" можуть додаватися по ходу роботи:

services['Розбити скло'] = "200 грн";

Умови виконання ДЗ

створити метод price(), який обчислює та повертає загальну вартість "наданих послуг".
створити метод minPrice() - який повертає мінімальний price
створити метод maxPrice() - який повертає максимальний price */

let services = {
  'стрижка': '60 грн',
  'гоління': '80 грн',
  'миття голови': '100 грн',

  // Метод для підрахунку загальної вартості всіх послуг
  price: function() {
    let sum = 0; // змінна для зберігання суми
    for (let key in this) { // перебираємо всі властивості об'єкта
      if (typeof this[key] === 'string') { // беремо тільки ті, що є рядками (ціни)
        sum += parseInt(this[key]); // перетворюємо рядок "60 грн" у число 60 і додаємо до суми
      }
    }
    return sum; // повертаємо загальну суму
  },

  // Метод для знаходження мінімальної ціни
  minPrice: function() {
    let min = null; // початкове значення мінімуму
    for (let key in this) {
      if (typeof this[key] === 'string') {
        let value = parseInt(this[key]);
        if (min === null || value < min) { // якщо min ще null або знайдено менше число
          min = value; // оновлюємо мінімальне значення
        }
      }
    }
    return min;
  },

  // Метод для знаходження максимальної ціни
  maxPrice: function() {
    let max = null;
    for (let key in this) {
      if (typeof this[key] === 'string') {
        let value = parseInt(this[key]);
        if (max === null || value > max) { // якщо max ще null або знайдено більше число
          max = value; // оновлюємо максимальне значення
        }
      }
    }
    return max;
  }
};

// Додаємо нову послугу після створення об'єкта
services['Фарбування волосся'] = '200 грн';
services['Стрижка бороди'] = '250 грн';


// Виводимо результати у консоль
console.log('Загальна вартість:', services.price(), 'грн'); // загальна сума
console.log('Мінімальна ціна:', services.minPrice(), 'грн'); // найменша ціна
console.log('Максимальна ціна:', services.maxPrice(), 'грн'); // найбільша ціна